    fun convert(text: String): String {
        if (text.isEmpty()) return text
        val sb = StringBuilder(text.length)
        for (ch in text) {
            sb.append(MAP[ch] ?: ch)
        }
        return sb.toString()
    }

    fun containsTraditional(text: String): Boolean {
        return text.any { MAP[it] != null && MAP[it] != it }
    }
